// Define your app's color palettes
private val DarkColorScheme = darkColorScheme(
    primary = Purple80, // Example color
    secondary = PurpleGrey80, // Example color
    tertiary = Pink80 // Example color
    // You can define other colors like background, surface, onPrimary, etc.
)

private val LightColorScheme = lightColorScheme(
    primary = Purple40, // Example color
    secondary = PurpleGrey40, // Example color
    tertiary = Pink40 // Example color

    /* Other default colors to override
    background = Color(0xFFFFFBFE),
    surface = Color(0xFFFFFBFE),
    onPrimary = Color.White,
    onSecondary = Color.White,
    onTertiary = Color.White,
    onBackground = Color(0xFF1C1B1F),
    onSurface = Color(0xFF1C1B1F),
    */
)

@Composable
fun StellarEyesTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic coloring is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb() // Or your desired status bar color
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography, // Assuming you also have a Typography.kt
        content = content
    )
}